// Desde ES7 se agregó la función de verificar si un array contiene un valor en específico
// El valor buscado puede ser un número o un string
// Y nos devolverá true o false según se encuentre el valor buscado o no

let numbers = [2, 8, 6, 5, 9, 1]; // A partir de una lista de valores...

console.log(numbers.includes(9)); // ...podemos verificar si se encuentra un valor en particular (en este caso el 9)